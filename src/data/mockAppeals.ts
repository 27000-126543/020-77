function randomItem<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

function randomInt(min: number, max: number): number {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

function generateDate(daysAgo: number): string {
  const now = new Date();
  now.setDate(now.getDate() - daysAgo);
  now.setHours(randomInt(6, 23), randomInt(0, 59), randomInt(0, 59));
  return now.toISOString();
}

function generateAppeal(id: number): Appeal {
  const topic = randomItem(topics);
  const template = appealTemplates[topic];
  const title = randomItem(template.titles);
  const content = randomItem(template.contents);
  const sentiment = randomItem(sentiments);
  const daysAgo = randomInt(0, 2);

  return {
    id: `AP-${String(id).padStart(4, '0')}`,
    title,
    content,
    platform: platforms[(id - 1) % platforms.length],
    topic,
    sentiment,
    createdAt: generateDate(daysAgo),
    likes: randomInt(0, 500),
    comments: randomInt(0, 200),
    shares: randomInt(0, 80),
    street: randomItem(streets),
    author: randomItem(authors),
  };
}

